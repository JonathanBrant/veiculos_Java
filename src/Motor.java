public class Motor {
    private int cilindros;
    private String torque;
    private String aspiração;
    private String  disposição;


    public void setCilindros(int cilindros) {
        this.cilindros = cilindros;
    }

    public void setTorque(String torque) {
        this.torque = torque;
    }

    public void setAspiração(String aspiração) {
        this.aspiração = aspiração;
    }

    public void setDisposição(String disposição) {
        this.disposição = disposição;
    }
}
